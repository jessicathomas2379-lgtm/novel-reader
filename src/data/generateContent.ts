function seededRandom(seed: number): () => number {
  let s = seed;
  return () => {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    return s / 0x7fffffff;
  };
}

export function generateChapterContent(novelIndex: number, chapterIndex: number): string {
  const random = seededRandom(novelIndex * 100 + chapterIndex);
  let content = '';
  let wordCount = 0;

  while (wordCount < 1000) {
    const starterIdx = Math.floor(random() * paragraphStarters.length);
    let paragraph = paragraphStarters[starterIdx] + '，';

    const sentenceCount = 3 + Math.floor(random() * 4);
    for (let i = 0; i < sentenceCount; i++) {
      const idx = Math.floor(random() * sentences.length);
      paragraph += sentences[idx];
    }

    content += paragraph + '\n\n';
    wordCount += paragraph.length;
  }

  return content.trim();
}
