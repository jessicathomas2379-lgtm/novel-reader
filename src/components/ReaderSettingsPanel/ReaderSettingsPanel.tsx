/**
 * ReaderSettingsPanel 组件 Props 接口
 */
export interface ReaderSettingsPanelProps {
  /** 是否可见 */
  visible: boolean;
  /** 当前是否为暗色模式 */
  isDarkMode: boolean;
  /** 返回按钮回调 */
  onBack: () => void;
  /** 切换夜间模式回调 */
  onToggleDarkMode: () => void;
  /** 点击面板外区域关闭回调 */
  onClose: () => void;
}

/**
 * ReaderSettingsPanel 阅读器设置面板组件
 */
export function ReaderSettingsPanel({
  visible,
  isDarkMode,
  onBack,
  onToggleDarkMode,
  onClose,
}: ReaderSettingsPanelProps) {
  const handleMiddleClick = useCallback(() => {
    onClose();
  }, [onClose]);

  const handlePanelClick = useCallback((e: React.MouseEvent) => {
    e.stopPropagation();
  }, []);

  if (!visible) {
    return null;
  }

  const nightButtonClassName = [
    styles.settingButton,
    isDarkMode ? styles.active : '',
  ]
    .filter(Boolean)
    .join(' ');

  return (
    <div
      className={styles.overlay}
      role="presentation"
      aria-hidden="false"
    >
      {/* 顶部栏 */}
      <div className={styles.topBar} onClick={handlePanelClick} role="dialog" aria-modal="true" aria-label="阅读器设置">
        <button
          className={styles.backButton}
          onClick={onBack}
          type="button"
          aria-label="返回"
        >
          <IconArrowLeft width={18} height={18} />
        </button>
        <span className={styles.topBarTitle}>设置</span>
        <div className={styles.topBarSpacer} />
      </div>

      {/* 中间透明区域 - 点击关闭 */}
      <div className={styles.middleArea} onClick={handleMiddleClick} />

      {/* 底部栏 */}
      <div className={styles.panel} onClick={handlePanelClick}>
        <div className={styles.content}>
          <button
            className={nightButtonClassName}
            onClick={onToggleDarkMode}
            type="button"
            aria-label={isDarkMode ? '关闭夜间模式' : '开启夜间模式'}
            aria-pressed={isDarkMode}
          >
            {isDarkMode ? (
              <IconDay width={24} height={24} />
            ) : (
              <IconNight width={24} height={24} />
            )}
            <span className={styles.settingLabel}>{isDarkMode ? '日间' : '夜间'}</span>
          </button>
        </div>
      </div>
    </div>
  );
}

export default ReaderSettingsPanel;
